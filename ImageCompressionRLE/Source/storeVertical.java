
public class storeVertical {

	private short counterVertical;
	private short oldValueVertical;
	
	// constructor using fields
	public storeVertical(short counterVertical, short oldValueVertical) {
		super();
		this.counterVertical = counterVertical;
		this.oldValueVertical = oldValueVertical;
	}
	
	// getters and setters for private classFields
	public short getCounterVertical() {
		return counterVertical;
	}
	public void setCounterVertical(short counterVertical) {
		this.counterVertical = counterVertical;
	}
	public short getOldValueVertical() {
		return oldValueVertical;
	}
	public void setOldValueVertical(short oldValueVertical) {
		this.oldValueVertical = oldValueVertical;
	}

	
	
	
}

